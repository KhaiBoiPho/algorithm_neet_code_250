from pathlib import Path # For object-oriented filesystem paths (better than os.path)

import datasets # HuggingFace Datasets: for loading standard datasets like GLUE
import evaluate # HuggingFace Evaluate: for comea]puting evaluation metrics
import torch
from accelerate import Accelerator # Core class for handling device placement and distributed training
from accelerate.logging import get_logger # Logging utiity from Accelerate
from accelerate.utils import set_seed # Utility to fix random seed for reproducibility
from datasets import load_dataset # Shortcut to load a dataset by name
from huggingface_hub import HfApi # Contact directly for Hugging Face Hub into coding needn't web interface
